package organisms;

public class Animals{
	
	private String sign;
	private int power;
	private int liveLength;
	private int health;
	private int powerToReproduce;
	
	public String getsign() {
		return sign;
	}
	public void setName(String sign) {
		this.sign = sign;
	}
	public int getPower() {
		return power;
	}
	public void setPower(int power) {
		this.power = power;
	}
	public int getLiveLength() {
		return liveLength;
	}
	public void setLiveLength(int liveLength) {
		this.liveLength = liveLength;
	}
	public int getHealth() {
		return health;
	}
	public void setHealth(int health) {
		this.health = health;
	}
	public int getPowerToReproduce() {
		return powerToReproduce;
	}
	public void setPowerToReproduce(int powerToReproduce) {
		this.powerToReproduce = powerToReproduce;
	}
	


}
